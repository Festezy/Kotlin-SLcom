package example.function

fun main() {
    /** A function is a group of related code that is used to perform a task.

    We have seen some functions in action: for example, the println()

    function is used to output text to the screen:
     */
    println("Hello")

    /**
     * The name in front of the parentheses is the function name,
     * and the comma-separated values inside the parentheses are function arguments.
     *
     * In the example above, println is the function name, "Hello" is the argument.
     * */
}