package example.function


/**
 * You can define your own functions and use them in your code.
 * Functions are defined using the fun keyword, followed by the name of the function:
 * */
fun welcome() {
    println("Hey there")
}

/**
 * The code above defines a function named welcome, which has just a single print statement.
 * The code in the function is called the function's body.
 *
 * After we have defined our function, we can call it in our code:
 * */
fun main() {
    welcome()
}

/**
 * To call the function, we need to use its name followed by parentheses.
 * You can call the same function as many times as you need.
 * */